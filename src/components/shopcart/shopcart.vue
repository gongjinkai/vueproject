<template>
<div class="shopcart">
  <div class="shopcart-content" @click="toggleList">
    <div class="content-left">
      <div class="logo-wrapper">
        <div class="logo" :class="{'highlight':totalCount>0}">
          <i class="icon iconfont icon-gouwuchekong" :class="{'highlight':totalCount>0}"></i>
        </div>
        <div class="num">{{totalCount}}</div>
      </div>
      <div class="price" :class="{'highlight':totalCount>0}">￥{{totalPrice}}</div>
      <div class="desc">另收配送费￥{{deliveryPrice}}元</div>
    </div>
    <div class="content-right">
      <div class="pay" :class="payClass">
        {{payDesc}}
      </div>
    </div>
  </div>
  <div class="ball-container">
    <div v-for="ball in balls" v-show="ball.show" transition="drop" class="ball">
      <div class="inner"></div>
    </div>
  </div>
  <div class="shopcart-list" v-show="listShow">
    <div class="list-header">
      <h1 class="title">购物车</h1>
      <span class="empty">清空</span>
    </div>
    <div class="list-content" ref="listContent">
      <ul>
        <li class="food" v-for="food in selectFoods">
          <span class="name">{{food.name}}</span>
          <div class="price">
            <span>￥{{food.price*food.count}}</span>
          </div>
          <div class="cartcontrol-wrapper">
            <cartcontrol :food="food"></cartcontrol>
          </div>
        </li>
      </ul>
    </div>
  </div>
</div>
</template>
<script type="text/ecmascript-6">
  import BScroll from 'better-scroll'
  import cartcontrol from '../cartcontrol/cartcontrol'
export default {
  props: {
    selectFoods: {
      type: Array,
      default () {
        return [
          {
            price: 10,
            count: 1
          }
        ]
      }
    },
    deliveryPrice: {
      type: Number,
      default: 0
    },
    minPrice: {
      type: Number,
      default: 0
    }
  },
  data () {
    return {
      balls: [
        {
          show: false
        },
        {
          show: false
        },
        {
          show: false
        },
        {
          show: false
        },
        {
          show: false
        }
      ],
      fold: true
    }
  },
  computed: {
    totalPrice() {
      let total = 0;
      this.selectFoods.forEach((food) => {
        total += food.price * food.count;
      });
      return total;
    },
    totalCount() {
      let count = 0;
      this.selectFoods.forEach((food) => {
        count += food.count;
      });
      return count;
    },
    payDesc () {
      if (this.totalPrice === 0) {
        return `￥${this.minPrice}元起送`;
      } else if (this.totalPrice < this.minPrice) {
        let diff = this.minPrice - this.totalPrice;
        return `还差￥${diff}元起送`;
      } else {
        return '去结算';
      }
    },
    payClass () {
      if (this.totalPrice < this.minPrice) {
        return 'not-enough'
      } else {
        return 'enough'
      }
    },
    listShow () {
      if(!this.totalCount){
        this.fold = true
        return false
      }
      let show = !this.fold
      if(show){
        this.$nextTick(()=>{
          if(!this.scroll) {
            this.scroll = new BScroll(this.$refs.listContent, {
              click: true
            });
          } else {
            this.scroll.refresh()
          }
        });
      }
      return show
    }
  },
  methods: {
    toggleList () {
      if(!this.totalCount){
        return
      }
      this.fold = !this.fold
    }
  }
}
</script>
